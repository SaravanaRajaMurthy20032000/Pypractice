marks = int(input("Enter the Marks: "))

if marks >= 85:
    print(f"Grade-A is the marks for {marks}")
elif (marks < 84) and (marks >= 70):
    print(f"Grade-B is the marks for {marks}")
elif (marks < 69) and (marks >= 55):
    print(f"Grade-C is the marks for {marks}")
elif (marks < 54) and (marks >= 40):
    print(f"Grade-D is the marks for {marks}")
elif marks < 40:
    print(f"Grade-E is the marks for {marks} and Need an Improvement")



